    private class MyLocationListener implements LocationListener {
        @Override
        public void onLocationChanged(Location loc) {
            result.setText("");
            text.setText("");
            bar.setVisibility(View.GONE);

            String longitude = String.valueOf(loc.getLongitude());
            String latitude = String.valueOf(loc.getLatitude());

            String s = "Longitude: " + longitude + "\nLatitude: " + latitude;
            text.setText(s);

            String a, b;
            a = longitude.substring(0,6);
            b = latitude.substring(0,6);

            String check_a, check_b;

            for(int i = 0;  i < markersArray.size(); i++) {
                check_a = String.valueOf(markersArray.get(i).location.longitude);
                check_b = String.valueOf(markersArray.get(i).location.latitude);

                check_a = check_a.substring(0,6);
                check_b = check_b.substring(0,6);
                Log.i("Debug", check_a+"/"+check_b+"\n"+a+"/"+b);

                if(!isMapSet) {
                    CameraPosition position = new CameraPosition.Builder()
                            .target(new LatLng(loc.getLatitude(), loc.getLongitude()))
                            .zoom(17)
                            .build();

                    CameraUpdate update = CameraUpdateFactory.newCameraPosition(position);
                    map.animateCamera(update);
                    isMapSet = true;
                }

                if (a.equals(check_a) && b.equals(check_b)) {
                    result.setText("Match Found");
                    result.setTextColor(Color.RED);
                    break;
                } else {
                    result.setText("Match Not Found");
                    result.setTextColor(Color.GRAY);
                }
            }
        }

        @Override
        public void onProviderDisabled(String provider) {}

        @Override
        public void onProviderEnabled(String provider) {}

        @Override
        public void onStatusChanged(String provider, int status, Bundle extras) {}
    }

    private BitmapDescriptor vectorToBitmap(@DrawableRes int id, @ColorInt int color) {
        Drawable vectorDrawable = ResourcesCompat.getDrawable(getResources(), id, null);
        Bitmap bitmap = Bitmap.createBitmap(vectorDrawable.getIntrinsicWidth(),
                vectorDrawable.getIntrinsicHeight(), Bitmap.Config.ARGB_8888);
        Canvas canvas = new Canvas(bitmap);
        vectorDrawable.setBounds(0, 0, canvas.getWidth(), canvas.getHeight());
        DrawableCompat.setTint(vectorDrawable, color);
        vectorDrawable.draw(canvas);
        return BitmapDescriptorFactory.fromBitmap(bitmap);
    }

